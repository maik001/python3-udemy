# cria a iteração de 1 até 10, range(i,j) = i+1, i+2,... j-1
for i in range(1, 11):
    print(f'i = {i}')

for j in range(10):
    print(f'j = {j}')

# cria duas iterações for de 1 até 10, simulando uma tabuada de multiplicação
for x in range(1, 11):
    for y in range(1, 11):
        print(f'{x} * {y} = {x * y}')

